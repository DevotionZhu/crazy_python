# 元素都是数值的元组
a_tuple = (20,10,-2,15.2,102,50)
# 计算最大值
print(max(a_tuple))
# 计算最小值
print(min(a_tuple))
# 计算长度
print(len(a_tuple))
# 元素都是字符串的列表
